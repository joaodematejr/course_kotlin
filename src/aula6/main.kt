
fun main() {
    /*
    Estrutura Condicional if/else
    */

    // Se (if) a condição for verdadeira, executa o bloco de código
    // Senão (else), executa o bloco de código

    var age = 18

    if (age <= 10) {
       println("O usuário é uma criança")
    } else if (age <= 17) {
       println("O usuário é adolescente")
    } else {
        println("O usuário é menor de idade")
    }


}