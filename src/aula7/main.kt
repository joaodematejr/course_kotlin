
fun main() {
    /*
    Operadores Lógicos

    && -> AND -> E -> Se ambos os lados forem verdadeiros
    || -> OR -> OU -> Se um dos lados for verdadeiro
    '!' -> NOT -> NÃO -> Se o lado for falso

     */
    */
    // True && True -> True
    if (20 == 20 && 10 < 20) {
       println("True")
    } else {
        println("False")
    }

    
    // True || False -> True    
    // False || False -> False    
    // True || True -> True
    if (20 == 20 && 10 < 20) {
       println("True")
    } else {
        println("False")
    }


}