
fun main(args: Array<String>) {

    val c: Char = 'a';
    val i: Int = 10;
    val f: Float = 10.0F;
    val s: String = "Ola Mundo";
    val d: Double = 10.0;
    val l: Long = 1000000000000000000;

    println("Char: ${c}");
    println("Int: ${i}");
    println("Float: ${f}");
    println("String: ${s}");
    println("Double: ${d}");
    println("Long: ${l}");
   
}